function getRewards()
{
    
    var cashRewards = document.getElementById("get-cash-rewards").value;
    var rewardPoints = document.getElementById("get-reward-points").value;
    var remarks = document.getElementById("get-remarks").value;

    
    document.getElementById("set-cash-rewards").value = cashRewards;
    document.getElementById("set-reward-points").value = rewardPoints;
    document.getElementById("set-remarks").value = remarks;

}

